package com.techhub.javasedemo.langpackage;

public class MathClassDemo {

	public static void main(String[] args) {

		System.out.println("PI = "+Math.PI);
		System.out.println("abs(Number) : "+Math.abs(-34.9));
		System.out.println("acos(Number) : "+Math.acos(40));
		System.out.println("addExact(Number,Number) : "+Math.addExact(40,60));
		System.out.println("subtractExact(Number,Number) : "+Math.subtractExact(40,60));
		System.out.println("multiplyExact(Number,Number) : "+Math.multiplyExact(40,60));
		System.out.println("incrementExact(Number,Number) : "+Math.incrementExact(40));
		System.out.println("ceil(Number) : "+Math.ceil(4.2));
		System.out.println("floor(Number) : "+Math.floor(4.9));
		System.out.println("log(Number) : "+Math.log(10));
		System.out.println("sin(Number) : "+Math.sin(10));
		System.out.println("cos(Number) : "+Math.cos(10));
		System.out.println("tan(Number) : "+Math.tan(10));
		System.out.println("min(Number) : "+Math.min(10,20));
		System.out.println("max(Number) : "+Math.max(10,20));
		System.out.println("sqrt(Number) : "+Math.sqrt(6));
		System.out.println("pow(Number) : "+Math.pow(2,3));
		System.out.println("round(Number) : "+Math.round(5.4));
		System.out.println("round(Number) : "+Math.round(5.5));
		System.out.println("round(Number) : "+Math.round(5.6));
		System.out.println("random() : "+Math.random());
		
	}

}
